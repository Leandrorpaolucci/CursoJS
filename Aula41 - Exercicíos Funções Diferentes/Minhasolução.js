function maiorNumero(num1, num2) {
  if (num1 > num2) {
    console.log(
      `O primeiro número digitado ${num1} é maior que o segundo número digitado ${num2}`
    );
  } else {
    console.log(
      `O segundo número digitado ${num2} é maior que o primeiro número digitado ${num1}`
    );
  }
}

maiorNumero(5, 4);